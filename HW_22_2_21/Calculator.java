package com.company;

public class Calculator {

    public double add(double a, double b)
    {
        return a + b;
    }

    public double multi(double a, double b)
    {
        return a * b;
    }

    public double min(double a, double b)
    {
        return a - b;
    }

    public double div(double a, double b)
    {
        return a / b;
    }
}

